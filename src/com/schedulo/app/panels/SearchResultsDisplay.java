    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        // TODO add your handling code here:
        String selectedOption = jList1.getSelectedValue();
        if(selectedOption == null){
            return;
        }
        ScoredObject so = (ScoredObject) InstanceData.fetch("search_score" + selectedOption);
        List<JsonObject> listOfCourses = (List<JsonObject>) so.getObject();
        ScheduleObject schObj = OneJsonUtil.jsonObjToScheduleObj(listOfCourses);
        WindowUtil.OpenTemporaryFrame("disp", new CalendarPanel(schObj));
    }//GEN-LAST:event_jButton1MouseClicked


