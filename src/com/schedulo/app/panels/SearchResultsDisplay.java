    private void jList1ValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_jList1ValueChanged
        // TODO add your handling code here:
        String selectedOption = jList1.getSelectedValue();
        if(selectedOption == null){
            resetLabels();
            return;
        }
        ScoredObject so = (ScoredObject) InstanceData.fetch("search_score" + selectedOption);
        highestSubscore.setText("Highest Subscore: " + String.valueOf(so.getHighestSubscore()));
        lowestSubscore.setText("Lowest Subscore: " + String.valueOf(so.getLowestSubscore()));
        scoreLabel.setText("Score: " + so.tabulate());
    }//GEN-LAST:event_jList1ValueChanged

