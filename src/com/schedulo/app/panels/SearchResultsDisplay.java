    public SearchResultsDisplay(List<ScoredObject> scores) {
        initComponents();
        
        AbstractListModel alm = (AbstractListModel<String>) jList1.getModel();
        String[] toSet = new String[scores.size()];
        int index = 0;
        for(ScoredObject so : scores){
            String key = "Schedule " + String.valueOf(so.index) + " ->   " + String.valueOf(so.tabulate()).substring(0, 5);
            InstanceData.store("search_score" + key, so);
            toSet[index] = key;
            index += 1;
        }
        jList1.setListData(toSet);
    }

